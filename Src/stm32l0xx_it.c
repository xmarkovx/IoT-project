void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
  static uint8_t i = 0;

  // Za napon baterije
  if (htim->Instance == TIM7) {
    g_send_batt_data = 1;
  }

  // Za LED periodicko izmjenjivanje
  else if (htim->Instance == TIM6) {
	  led_pwm_all_off();
	  led_pwm_set(g_led_id[i], g_led_output_percentage[i]);

	  if (g_led_change_type == FOUR_LED_GPIO_CHANGE) {
		  i++;
		  if (i == LED_NUMBER) {
			  i = 0;
		  }
	  }
	  else if (g_led_change_type == TWO_LED_PWM_CHANGE) {
		  i++;
		  if (i == 2) {
			  i = 0;
		  }
	  }
  }

}

